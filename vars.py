#Forma de declarar variables

'''
    Hay distintas formas de declarar variables,
    las formas mas comunes son las convenciones:

    snake_case: nombre_variable
    camelCase: nombreVariable
    PascalCase: NombreVariable
    UPPER_CASE: NOMBRE_VARIABLE

    Las variables no pueden comenzar con numeros,
    no pueden contener espacios o caracteres especiales,
    por eso se suelen usar nombres descriptivos y claros,
    siguiendo las convenciones de nombres de variables.

'''


'''
    Normalmente al escribir una variable, se suele sigue 
    el mismo patron, para que de esta forma se pueda
    identificar que variable es.
    En casos de variables con un valor importante alto, 
    se suelen poner en mayusculas, para que se pueda
    identificar facilmente.
'''

#Este valor no es una variable, es una constante, por lo tanto no cambia 

USER_ID = 937363976293728764

#Este valor es una variable, por lo tanto si puede ser cambiado 
tokenUser = 'ijfew78g54ij0ey8gg4'

#Segunda forma de asignar valores a variables
'''
    En python, se pueden asignar valores a variables
    de forma mas sencilla, poniendo el orden separado de una coma 
    y un espacio para asignar el valor a la variable en el orden
    en el que se encuentran.
'''

nameMovie, yearMovie = 'El Padrino', 1972


